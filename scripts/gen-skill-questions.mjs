let total = 0;
for (const block of QUESTIONS) {
  if (!block.skill || !Array.isArray(block.items)) {
    throw new Error(`Bad block: ${JSON.stringify(block).slice(0, 80)}`);
  }
  if (block.items.length !== 10) {
    throw new Error(`Skill "${block.skill}" has ${block.items.length} questions (need 10).`);
  }
  for (const it of block.items) {
    const [q, opts, ans, expl, diff] = it;
    if (typeof q !== "string" || !q.length) throw new Error(`Bad question in ${block.skill}`);
    if (!Array.isArray(opts) || opts.length !== 4) throw new Error(`Bad options in ${block.skill}: ${q}`);
    if (!Number.isInteger(ans) || ans < 0 || ans > 3) {
      throw new Error(`Bad answer_index in ${block.skill}: ${q} -> ${ans}`);
    }
    if (typeof expl !== "string") throw new Error(`Bad explanation in ${block.skill}`);
    if (!["Easy", "Medium", "Hard"].includes(diff)) {
      throw new Error(`Bad difficulty in ${block.skill}: ${diff}`);
    }
  }
  total += block.items.length;
}

